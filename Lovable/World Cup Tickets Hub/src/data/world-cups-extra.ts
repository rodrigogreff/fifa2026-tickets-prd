// Dados adicionais das Copas: seleções participantes + escalações da final.
// Separado de world-cups.ts para manter cada arquivo gerenciável.
// Conteúdo factual público (participações em torneios, escalações de finais).

export interface NationRef {
  name: string;
  iso: string; // ISO 3166 lowercase para flagcdn (gb-eng/gb-sct/gb-wls especiais)
}

export interface FinalPlayer {
  name: string;
  goals?: number;        // gols regulares marcados PARA a própria seleção
  ownGoals?: number;     // gols contra (contam para a seleção ADVERSÁRIA)
}

export interface FinalLineup {
  home: { team: string; iso: string; coach?: string; players: FinalPlayer[] };
  away: { team: string; iso: string; coach?: string; players: FinalPlayer[] };
}

